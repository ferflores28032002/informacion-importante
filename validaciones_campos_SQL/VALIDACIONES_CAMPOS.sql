CREATE TABLE VALIDACIONES_CAMPO (
	TELEFONO_CON_NUMERO_DE_SALIDA CHAR(23) CONSTRAINT ck_telephone_con_numero_de_salida CHECK (TELEFONO_CON_NUMERO_DE_SALIDA  LIKE '+[0-9][0-9][0-9] [0-9][0-9][0-9][0-9]-[0-9][0-9][0-9][0-9]'),
	TELEFONO_SIN_NUMERO_DE_SALIDA CHAR(8) constraint ck_telephone_validacion CHECK (TELEFONO_SIN_NUMERO_DE_SALIDA like '[2|5|7|8][0-9][0-9][0-9][0-9][0-9][0-9][0-9]') NOT NULL, ---PUEDE COMENZAR EN 2,5,7,8 SEGUIDO DE LOS OTROS 7 NUMEROS
	CORREO VARCHAR(100) constraint ck_correo_validacion CHECK (CORREO like '_%@__%.__%') NOT NULL,
	CEDULA VARCHAR (16) constraint ck_cedula_validacion  CHECK (CEDULA LIKE '[0-9][0-9][0-9]-[0-9][0-9][0-9][0-9][0-9][0-9]-[0-9][0-9][0-9][0-9][A-Z]') NOT NULL,
	CODIGO_EMPLEADO VARCHAR(10) CHECK(CODIGO_EMPLEADO LIKE '[A-Z][A-Z]-[0-9][0-9][0-9][0-9]-[0-9][0-9]') NOT NULL,
	TARGETA_CREDITO VARCHAR(20) CHECK (TARGETA_CREDITO LIKE '[0-9][0-9][0-9][0-9] [0-9][0-9][0-9][0-9] [0-9][0-9][0-9][0-9] [0-9][0-9][0-9][0-9]') NOT NULL
);

